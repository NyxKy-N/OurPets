export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

export function normalizeLocale(value: string | null | undefined): Locale {
  if (!value) return defaultLocale;
  const normalized = value.toLowerCase();

  if (isLocale(normalized)) return normalized;

  const baseLocale = normalized.split("-")[0];
  if (isLocale(baseLocale)) return baseLocale;

  if (normalized.startsWith("zh")) return "zh";
  if (normalized.startsWith("it")) return "it";
  if (normalized.startsWith("es")) return "es";

  return defaultLocale;
}

export function detectLocaleFromAcceptLanguage(headerValue: string | null | undefined): Locale {
  if (!headerValue) return defaultLocale;

  const accepted = headerValue
    .split(",")
    .map((item) => item.split(";")[0]?.trim())
    .filter(Boolean);

  for (const candidate of accepted) {
    const locale = normalizeLocale(candidate);
    if (locale) return locale;
  }

  return defaultLocale;
}

export function getMessages(locale: Locale): Messages {
  return messages[locale];
}

export function getPetTypeLabel(locale: Locale, type: "DOG" | "CAT" | "OTHER") {
  const formMessages = getMessages(locale).form;
  if (type === "DOG") return formMessages.dog;
  if (type === "CAT") return formMessages.cat;
  return formMessages.other;
}

export function getPetGenderLabel(locale: Locale, gender: "MALE" | "FEMALE" | "UNKNOWN" | null | undefined) {
  const formMessages = getMessages(locale).form;
  if (gender === "MALE") return formMessages.male;
  if (gender === "FEMALE") return formMessages.female;
  return formMessages.unknownGender;
}

function getAgeParts(birthDate: string | Date) {
  const date = new Date(birthDate);
  if (Number.isNaN(date.getTime())) return null;

  const now = new Date();
  let years = now.getUTCFullYear() - date.getUTCFullYear();
  let months = now.getUTCMonth() - date.getUTCMonth();

  if (months < 0) {
    years -= 1;
    months += 12;
  }

  if (years < 0) return null;

  return { years, months };
}

export function formatPetAge(locale: Locale, birthDate?: string | Date | null, fallbackAge?: number | null) {
  if (birthDate) {
    const parts = getAgeParts(birthDate);
    if (parts) {
      if (locale === "zh") {
        return `${parts.years}岁${parts.months}个月`;
      }

      const yearUnit = locale === "it" ? (parts.years === 1 ? "anno" : "anni") : locale === "es" ? (parts.years === 1 ? "año" : "años") : parts.years === 1 ? "year" : "years";
      const monthUnit = locale === "it" ? (parts.months === 1 ? "mese" : "mesi") : locale === "es" ? (parts.months === 1 ? "mes" : "meses") : parts.months === 1 ? "month" : "months";
      return `${parts.years} ${yearUnit} ${parts.months} ${monthUnit}`;
    }
  }

  const safeAge = fallbackAge ?? 0;
  const pr = new Intl.PluralRules(locale);
  const unitByLocale: Record<Locale, Record<Intl.LDMLPluralRule, string>> = {
    en: { zero: "years", one: "year", two: "years", few: "years", many: "years", other: "years" },
    zh: { zero: "岁", one: "岁", two: "岁", few: "岁", many: "岁", other: "岁" },
    it: { zero: "anni", one: "anno", two: "anni", few: "anni", many: "anni", other: "anni" },
    es: { zero: "años", one: "año", two: "años", few: "años", many: "años", other: "años" },
  };
  const rule = pr.select(safeAge);
  const unit = unitByLocale[locale][rule] ?? unitByLocale[locale].other;

  if (locale === "zh") return `${safeAge}${unit}`;

  return `${safeAge} ${unit}`;
}

export function formatCompactLabel(locale: Locale, value: number, unit: string) {
  if (locale === "zh") return `${value}${unit}`;
  return `${value} ${unit}`;
}

export function formatDateTime(locale: Locale, value: string | Date) {
  return new Intl.DateTimeFormat(locale, {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(new Date(value));
}
